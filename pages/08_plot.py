# Data Loading Functions
# ================================

def load_psyche_scores(firebase_ref):
    """Load PSYCHE scores from Firebase for all experiments
    
    PSYCHE scores are generated by the Evaluation page (ì—°êµ¬ììš© ê²©ë¦¬ í´ë”) 
    and saved with key pattern: psyche_{diagnosis}_{model}_{exp_number}
    under clients/{client_number}/
    """
    scores_data = []
    
    for disorder, models in EXPERIMENTS.items():
        for model_type, experiments in models.items():
            for client_num, exp_num in experiments:
                # Construct the evaluation key
                # Format: psyche_mdd_gptbasic_1111 (lowercase, no underscores in model name)
                model_name_normalized = model_type.replace('_', '')  # gpt_basic -> gptbasic
                eval_key = f"psyche_{disorder.lower()}_{model_name_normalized}_{exp_num}"
                
                # Load from clients/{client_number}/{eval_key}
                data = firebase_ref.child(f"clients_{client_num}").child(eval_key).get()
                
                if data and 'psyche_score' in data:
                    psyche_score = data['psyche_score']
                    scores_data.append({
                        'disorder': disorder,
                        'model': model_type,
                        'client_num': client_num,
                        'exp_num': exp_num,
                        'psyche_score': psyche_score
                    })
    
    return scores_data


